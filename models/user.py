class User():

    def __init__(self, id, bio, created_on, active, first_name, last_name, email, username, password):
        self.id = id
        self.bio = bio
        self.created_on = created_on
        self.active = active
        self.first_name = first_name
        self.last_name = last_name
        self.email = email
        self.username = username
        self.password = password

